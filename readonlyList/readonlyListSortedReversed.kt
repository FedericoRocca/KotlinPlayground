fun main() {
    val colors = listOf("green", "orange", "blue")
    println("Original colors list: $colors")
    println("Reversed colors list: ${colors.reversed()}")
    println("Sorted colors list: ${colors.sorted()}")
    
    val oddNumbers = listOf(5, 3, 7, 1)
    println("Original numbers list: $oddNumbers")
    println("Sorted number list: ${oddNumbers.sorted()}")
}
